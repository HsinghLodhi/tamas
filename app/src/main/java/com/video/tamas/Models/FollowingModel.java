package com.video.tamas.Models;

public class FollowingModel {
    String followingId;
    String followingName;
    String followingPic;

    public String getfollowingId() {
        return followingId;
    }

    public void setfollowingId(String followerId) {
        this.followingId = followerId;
    }

    public String getfollowingName() {
        return followingName;
    }

    public void setfollowingName(String followerName) {
        this.followingName = followerName;
    }

    public String getfollowingPic() {
        return followingPic;
    }

    public void setfollowingPic(String followerPic) {
        this.followingPic = followerPic;
    }
}
